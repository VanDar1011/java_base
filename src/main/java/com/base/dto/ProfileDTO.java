package com.base.dto;

public class ProfileDTO {
    private int id;
    private String address;

    public ProfileDTO() {
    }

    public ProfileDTO(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
